def add_num(num1, num2):
    result = num1 + num2
    print(f"Результат додавання {num1}+{num2} =  {result}")

def sub_num(num1, num2):
    result = num1 - num2
    print(f"Результат віднімання: {num1} - {num2} ={result}")


def mul_num(num1, num2):
    result = num1 * num2
    print(f"Результат множення: {result}")



def div_num(num1, num2):
    result = num1 / num2
    print(f"Результат ділення: {result}")






